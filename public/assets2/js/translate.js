function toggleLanguage() {
    const checkbox = document.getElementById('cb3-8');
    if (checkbox.checked) {
        setLanguage('en');
    } else {
        setLanguage('es');
    }
}

function setLanguage(lang) {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = traducciones[lang][key] || element.textContent;
        element.textContent = translation;
    });

    // Actualizar los placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translation = traducciones[lang][key] || element.placeholder;
        element.placeholder = translation;
    });

    document.querySelectorAll('input[type="submit"][data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = traducciones[lang][key] || element.value;
        element.value = translation;
    });

    localStorage.setItem("language", lang);
}

function toggleTheme() {
    const themeToggle = document.getElementById('theme-toggle');
    if (themeToggle.checked) {
        applyDarkMode();
    } else {
        applyLightMode();
    }
}

function applyDarkMode() {
    document.body.classList.add('dark-mode');
    localStorage.setItem('theme', 'dark');
}

function applyLightMode() {
    document.body.classList.remove('dark-mode');
    localStorage.setItem('theme', 'light');
}

function initTheme() {
    const savedTheme = localStorage.getItem('theme');
    const themeToggle = document.getElementById('theme-toggle');
    if (savedTheme === 'dark') {
        themeToggle.checked = true;
        applyDarkMode();
    } else {
        themeToggle.checked = false;
        applyLightMode();
    }
}

function confirmDelete(event) {
    event.preventDefault(); // Previene la acción predeterminada del enlace
    const confirmed = confirm("Estás a punto de eliminar este usuario, ¿Estás seguro?");
    if (confirmed) {
        window.location.href = event.currentTarget.href;
    }
}

// Cargar el idioma y el tema guardados al cargar la página
window.addEventListener('load', () => {
    const language = localStorage.getItem("language") || 'es';
    setLanguage(language);
    document.getElementById('cb3-8').checked = language === 'en';
    initTheme();
    document.getElementById('theme-toggle').addEventListener('change', toggleTheme);
});



